fn read_lines(txt: &str) -> Vec<String> {
    txt.lines()
        .map(|x| x.trim())
        .filter(|x| !x.is_empty())
        .map(|x| x.to_owned())
        .collect()
}

fn read_columns(lines: &[String]) -> Vec<Vec<char>> {
    let n_cols = lines[0].len();
    let mut cols: Vec<Vec<char>> = iter::repeat_with(Vec::new).take(n_cols).collect();
    for row in lines {
        for (j, ch) in row.chars().enumerate() {
            cols[j].push(ch);
        }
    }
    cols
}

fn score_col(col: &[char]) -> usize {
    let mut value_per_rock = col.len();
    let mut total: usize = 0;
    for (j, ch) in col.iter().enumerate() {
        if *ch == 'O' {
            total += value_per_rock;
            value_per_rock -= 1;
        } else if *ch == '#' {
            value_per_rock = col.len() - j - 1;
        }
    }
    total
}
